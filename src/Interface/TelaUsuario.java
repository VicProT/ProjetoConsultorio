    private void EnviarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnviarUsuarioActionPerformed
        if(NomeUsuario.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Preencha o NOME para continuar");
           NomeUsuario.requestFocus();
        }
        else if(LoginUsuario.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Preencha o LOGIN para continuar");
           LoginUsuario.requestFocus();
        }
        else if(SenhaUsuario.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Preencha a SENHA para continuar");
           SenhaUsuario.requestFocus();
        }
        else if(CSenhaUsuario.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Preencha o CONFIRMA SENHA para continuar");
           CSenhaUsuario.requestFocus();
        }else if(SenhaUsuario.getText().equals(CSenhaUsuario.getText())){
            if(flag == 1){        
            Mod.setNomeUsu(NomeUsuario.getText());
            Mod.setLoginUsu(LoginUsuario.getText());
            Mod.setSenhaUsu(SenhaUsuario.getText());
            Mod.setTipoUsu((String)TipoUsuario.getSelectedItem());
            Dao.Salvar(Mod);
        
        
           //LIMPA OS CAMPOS
           NomeUsuario.setText("");
           LoginUsuario.setText("");
           SenhaUsuario.setText("");
           CSenhaUsuario.setText("");
           IDUsuario.setText("");
           
           //BLOQUEIAS OS CAMPOS
           NomeUsuario.setEnabled(false);
           LoginUsuario.setEnabled(false);
           SenhaUsuario.setEnabled(false);
           CSenhaUsuario.setEnabled(false);
           TipoUsuario.setEnabled(false);       
           EnviarUsuario.setEnabled(false);
           NovoUsuario.setEnabled(true);
           EditarUsuario.setEnabled(false);
           CancelarUsuario.setEnabled(false);
        
          }else{
           Mod.setCoddenUsu(Integer.parseInt(IDUsuario.getText()));
           Mod.setNomeUsu(NomeUsuario.getText());
           Mod.setLoginUsu(LoginUsuario.getText());
           Mod.setSenhaUsu(SenhaUsuario.getText());
           Mod.setTipoUsu((String)TipoUsuario.getSelectedItem());
           Dao.Alteraroi(Mod);
        
           //LIMPA OS CAMPOS
           NomeUsuario.setText("");
           LoginUsuario.setText("");
           SenhaUsuario.setText("");
           CSenhaUsuario.setText("");
           IDUsuario.setText("");
        
           //BLOQUEIAS OS CAMPOS
           NomeUsuario.setEnabled(false);
           LoginUsuario.setEnabled(false);
           SenhaUsuario.setEnabled(false);
           CSenhaUsuario.setEnabled(false);
           TipoUsuario.setEnabled(false);       
           EnviarUsuario.setEnabled(false);
           NovoUsuario.setEnabled(true);
           EditarUsuario.setEnabled(false);
           CancelarUsuario.setEnabled(false);
        
           } 
        }else{JOptionPane.showMessageDialog(null, "SENHAS DEVEM SER IGUAIS");}
        PreencherTabela("select * from usuario order by Nomeusu");//CHAMA A TABELA LOGO AO ABRIR  
    }//GEN-LAST:event_EnviarUsuarioActionPerformed

