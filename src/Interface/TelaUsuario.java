    private void TabelaUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaUsuarioMouseClicked

        String Nomedent =""+TabelaUsuario.getValueAt(TabelaUsuario.getSelectedRow(),1);
        Conex.Conexao();
        Conex.ExecultaSQL("select * from usuario where Nomeusu ='"+Nomedent+"'");
        try {
            Conex.Rs.first();
            IDUsuario.setText(String.valueOf(Conex.Rs.getInt("Coddenusu")));
            NomeUsuario.setText(Conex.Rs.getString("Nomeusu"));
            LoginUsuario.setText(Conex.Rs.getString("Loginusu"));
            SenhaUsuario.setText(Conex.Rs.getString("Senhausu"));
            CSenhaUsuario.setText(Conex.Rs.getString("Senhausu"));
            TipoUsuario.setSelectedItem(Conex.Rs.getString("Tipousu"));
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Erro no Mouse USUARIO--\n\n"+ex);
        }
        Conex.Desconecta();
        
        
        NovoUsuario.setEnabled(false);
        EditarUsuario.setEnabled(true);
        ExcluirUsuario.setEnabled(true);
        CancelarUsuario.setEnabled(true);
        EnviarUsuario.setEnabled(false);
        
                
    }//GEN-LAST:event_TabelaUsuarioMouseClicked

