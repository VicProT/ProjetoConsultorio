    private void TabelaTelaMedicosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaTelaMedicosMouseClicked

        String Nomedent =""+TabelaTelaMedicos.getValueAt(TabelaTelaMedicos.getSelectedRow(),1);
        Conex.Conexao();
        Conex.ExecultaSQL("select * from dent where Nomedent ='"+Nomedent+"'");
        try {
            Conex.Rs.first();
            DigitarIDTelaMedicos.setText(String.valueOf(Conex.Rs.getInt("Codden")));
            DigitarNomeTelaMedicos.setText(Conex.Rs.getString("Nomedent"));
            DigitarRegistroTelaMedicos.setText(Conex.Rs.getString("Registro"));
            DigitarCPFTelaMedicos.setText(Conex.Rs.getString("Cpfden"));
            DigitarTelefoneTelaMedicos.setText(Conex.Rs.getString("Telden"));
            EscolherAreaTelaMedicos.setSelectedItem(Conex.Rs.getString("Areaden"));

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Erro no Mouse\n\n"+ex);
        }
        Conex.Desconecta();
        BotaorExcluirTelaMedicos.setEnabled(true);
        BotaorEditarTelaMedicos.setEnabled(true);

    }//GEN-LAST:event_TabelaTelaMedicosMouseClicked

    public void PreencherTabela(String Sql){
        ArrayList Dados = new ArrayList();
        String [] Colunas = new String[]{"CODIGO","NOME","REGISTRO","CPF","TELEFONE","AREA"};
        Conex.Conexao();
        Conex.ExecultaSQL(Sql);
        
        try{
            Conex.Rs.first();
            do{
                Dados.add(new Object[]{Conex.Rs.getInt("Codden"),Conex.Rs.getString("Nomedent"),Conex.Rs.getString("Registro"),Conex.Rs.getString("Cpfden"),Conex.Rs.getString("Telden"),Conex.Rs.getString("Areaden")});//LINHA
                
            }while(Conex.Rs.next());//TEM DADOS VAI PERCORRER
        }catch(SQLException ex){
            if(flag > 0){
                JOptionPane.showMessageDialog(rootPane,"Busque novo DENTISTA para preencher tabela"+ex);
            }
            
        }
        ModeloTabela Modelo = new ModeloTabela(Dados,Colunas);
        
        TabelaTelaMedicos.setModel(Modelo);
        TabelaTelaMedicos.getColumnModel().getColumn(0).setPreferredWidth(100);//TAMANHO E LARGURA COLUNA
        TabelaTelaMedicos.getColumnModel().getColumn(0).setResizable(false);//NAO MUDAR O TAMANHO DA COLUNA
        TabelaTelaMedicos.getColumnModel().getColumn(1).setPreferredWidth(200);//TAMANHO E LARGURA COLUNA
        TabelaTelaMedicos.getColumnModel().getColumn(1).setResizable(false);//NAO MUDAR O TAMANHO DA COLUNA
        TabelaTelaMedicos.getColumnModel().getColumn(2).setPreferredWidth(200);//TAMANHO E LARGURA COLUNA
        TabelaTelaMedicos.getColumnModel().getColumn(2).setResizable(false);//NAO MUDAR O TAMANHO DA COLUNA
        TabelaTelaMedicos.getColumnModel().getColumn(3).setPreferredWidth(200);//TAMANHO E LARGURA COLUNA
        TabelaTelaMedicos.getColumnModel().getColumn(3).setResizable(false);//NAO MUDAR O TAMANHO DA COLUNA
        TabelaTelaMedicos.getColumnModel().getColumn(4).setPreferredWidth(200);//TAMANHO E LARGURA COLUNA
        TabelaTelaMedicos.getColumnModel().getColumn(4).setResizable(false);//NAO MUDAR O TAMANHO DA COLUNA
        TabelaTelaMedicos.getColumnModel().getColumn(5).setPreferredWidth(200);//TAMANHO E LARGURA COLUNA
        TabelaTelaMedicos.getColumnModel().getColumn(5).setResizable(false);//NAO MUDAR O TAMANHO DA COLUNA
        
        TabelaTelaMedicos.getTableHeader().setReorderingAllowed(false);//NAO REORDENA CABECALHO
        TabelaTelaMedicos.setAutoResizeMode(TabelaTelaMedicos.AUTO_RESIZE_OFF);//NAO REDIME TABELA
        TabelaTelaMedicos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);//SO SELECIONA UM DADO POR VEZ
        
        Conex.Desconecta();
    }
    
    
    
