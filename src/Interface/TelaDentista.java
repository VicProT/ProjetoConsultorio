    private void BotaorEnviarTelaMedicosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaorEnviarTelaMedicosActionPerformed
        // TODO add your handling code here:
        
        if(DigitarNomeTelaMedicos.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Preencha o NOME para continuar");
           DigitarNomeTelaMedicos.requestFocus();
        }
        else if(DigitarRegistroTelaMedicos.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Preencha o REGISTRO para continuar");
           DigitarRegistroTelaMedicos.requestFocus();
        }
        else if(DigitarCPFTelaMedicos.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Preencha o CPF para continuar");
           DigitarCPFTelaMedicos.requestFocus();
        }
        else if(DigitarTelefoneTelaMedicos.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Preencha o TELEFONE para continuar");
           DigitarTelefoneTelaMedicos.requestFocus();
        }else
        
        if(flag == 1){
        Mod.setNomeDent(DigitarNomeTelaMedicos.getText());
        Mod.setAreaDent((String)EscolherAreaTelaMedicos.getSelectedItem());
        Mod.setRegDent(DigitarRegistroTelaMedicos.getText());
        Mod.setCpfDent(DigitarCPFTelaMedicos.getText());
        Mod.setTelDent(DigitarTelefoneTelaMedicos.getText());
        Cont.Salvar(Mod);
        
        
        //LIMPA OS CAMPOS
        DigitarNomeTelaMedicos.setText("");
        DigitarRegistroTelaMedicos.setText("");
        DigitarCPFTelaMedicos.setText("");
        DigitarTelefoneTelaMedicos.setText("");
        
        //BLOQUEIAS OS CAMPOS
        DigitarNomeTelaMedicos.setEnabled(false);
        DigitarRegistroTelaMedicos.setEnabled(false);
        DigitarCPFTelaMedicos.setEnabled(false);
        DigitarTelefoneTelaMedicos.setEnabled(false);
        EscolherAreaTelaMedicos.setEnabled(false);       
        BotaorEnviarTelaMedicos.setEnabled(false);
        
        }
        else{
            Mod.setCodDent((Integer.parseInt(DigitarIDTelaMedicos.getText())));
            Mod.setNomeDent(DigitarNomeTelaMedicos.getText());
            Mod.setRegDent(DigitarRegistroTelaMedicos.getText());
            Mod.setCpfDent(DigitarCPFTelaMedicos.getText());
            Mod.setTelDent(DigitarTelefoneTelaMedicos.getText());
            Mod.setAreaDent((String)EscolherAreaTelaMedicos.getSelectedItem());
            
            Cont.Editar(Mod);
            
            DigitarNomeTelaMedicos.setEnabled(true);
            DigitarRegistroTelaMedicos.setEnabled(true);
            DigitarCPFTelaMedicos.setEnabled(true);
            DigitarTelefoneTelaMedicos.setEnabled(true);
            EscolherAreaTelaMedicos.setEnabled(true);       
            BotaorEnviarTelaMedicos.setEnabled(true);
            BotaorNovoMedicoTelaMedicos.setEnabled(true);
            
            
            DigitarNomeTelaMedicos.setText("");
            DigitarRegistroTelaMedicos.setText("");
            DigitarCPFTelaMedicos.setText("");
            DigitarTelefoneTelaMedicos.setText("");
       
            
            
        }
        PreencherTabela("select * from dent order by Nomedent");//CHAMA A TABELA LOGO AO ABRIR
    }//GEN-LAST:event_BotaorEnviarTelaMedicosActionPerformed

