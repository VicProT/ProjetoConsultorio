    private void TelaSecretarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TelaSecretarioMouseClicked
        String NomeSeecre=""+TelaSecretario.getValueAt(TelaSecretario.getSelectedRow(),1);
        Conex.Conexao();
        Conex.ExecultaSQL("select * from secretario where Nomesecre ='"+NomeSeecre+"'");
        try {
            Conex.Rs.first();
            IDSecretario.setText(String.valueOf(Conex.Rs.getInt("Coddensecre")));
            NomeSecretario.setText(Conex.Rs.getString("Nomesecre"));
            CPFSecretario.setText(Conex.Rs.getString("CPFsecre"));
            NumeroSecretario.setText(Conex.Rs.getString("Telsecre"));
            
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Erro no Mouse USUARIO--\n\n"+ex);
        }
        Conex.Desconecta();
        
        
        NovoSecretario.setEnabled(false);
        EditarSecretario.setEnabled(true);
        ExcluirSecretario.setEnabled(true);
        CancelarSecretario.setEnabled(true);
        EnviarSecretario.setEnabled(false);
    }//GEN-LAST:event_TelaSecretarioMouseClicked
  
    
    public void PreencherTabela(String Sql){
        ArrayList Dados = new ArrayList();
        String [] Colunas = new String[]{"ID","NOME","CPF","TELEFONE"};
        Conex.Conexao();
        Conex.ExecultaSQL(Sql);
        
        try{
            Conex.Rs.first();
            do{
                Dados.add(new Object[]{Conex.Rs.getInt("Coddensecre"),Conex.Rs.getString("Nomesecre"),Conex.Rs.getString("CPFsecre"),Conex.Rs.getString("Telsecre")});//LINHA
                
            }while(Conex.Rs.next());//TEM DADOS VAI PERCORRER
        }catch(SQLException ex){
            if(flag > 0){
                JOptionPane.showMessageDialog(rootPane,"Busque novo SECRETARIO para preencher tabela \n\n"+ex);
            }
        }
        ModeloTabela Modelo = new ModeloTabela(Dados,Colunas);
        
        TelaSecretario.setModel(Modelo);
        TelaSecretario.getColumnModel().getColumn(0).setPreferredWidth(50);//TAMANHO E LARGURA COLUNA
        TelaSecretario.getColumnModel().getColumn(0).setResizable(false);//NAO MUDAR O TAMANHO DA COLUNA
        TelaSecretario.getColumnModel().getColumn(1).setPreferredWidth(200);//TAMANHO E LARGURA COLUNA
        TelaSecretario.getColumnModel().getColumn(1).setResizable(false);//NAO MUDAR O TAMANHO DA COLUNA
        TelaSecretario.getColumnModel().getColumn(2).setPreferredWidth(150);//TAMANHO E LARGURA COLUNA
        TelaSecretario.getColumnModel().getColumn(2).setResizable(false);//NAO MUDAR O TAMANHO DA COLUNA
        TelaSecretario.getColumnModel().getColumn(3).setPreferredWidth(150);//TAMANHO E LARGURA COLUNA
        TelaSecretario.getColumnModel().getColumn(3).setResizable(false);//NAO MUDAR O TAMANHO DA COLUNA
                
        TelaSecretario.getTableHeader().setReorderingAllowed(false);//NAO REORDENA CABECALHO
        TelaSecretario.setAutoResizeMode(TelaSecretario.AUTO_RESIZE_OFF);//NAO REDIME TABELA
        TelaSecretario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);//SO SELECIONA UM DADO POR VEZ
        
        Conex.Desconecta();
    }
    
    
