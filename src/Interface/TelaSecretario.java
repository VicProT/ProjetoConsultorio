    private void EnviarSecretarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnviarSecretarioActionPerformed
        if(NomeSecretario.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Preencha o NOME para continuar");
           NomeSecretario.requestFocus();
        }
        else if(CPFSecretario.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Preencha o CPF para continuar");
           CPFSecretario.requestFocus();
        }
        else if(NumeroSecretario.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Preencha a TELEFONE para continuar");
           NumeroSecretario.requestFocus();
        }
        else if(flag == 1){        
            Mod.setNomeSecre(NomeSecretario.getText());
            Mod.setCpfSecre(CPFSecretario.getText());
            Mod.setTelSecre(NumeroSecretario.getText());
            Dao.Salvar(Mod);
        
        
           //LIMPA OS CAMPOS
           NomeSecretario.setText("");
           CPFSecretario.setText("");
           NumeroSecretario.setText("");
           IDSecretario.setText("");
           
           //BLOQUEIAS OS CAMPOS
           NomeSecretario.setEnabled(false);
           CPFSecretario.setEnabled(false);
           NumeroSecretario.setEnabled(false);
           IDSecretario.setEnabled(false);
           
               
           EnviarSecretario.setEnabled(false);
           NovoSecretario.setEnabled(true);
           EditarSecretario.setEnabled(false);
           CancelarSecretario.setEnabled(false);
        
          }else{
           Mod.setCodSecre(Integer.parseInt(IDSecretario.getText()));
           Mod.setNomeSecre(NomeSecretario.getText());
           Mod.setCpfSecre(CPFSecretario.getText());
           Mod.setTelSecre(NumeroSecretario.getText());
           Dao.AlterarSiim(Mod);
        
           //LIMPA OS CAMPOS
           NomeSecretario.setText("");
           CPFSecretario.setText("");
           NumeroSecretario.setText("");
           IDSecretario.setText("");
           
           //BLOQUEIAS OS CAMPOS
           NomeSecretario.setEnabled(false);
           CPFSecretario.setEnabled(false);
           NumeroSecretario.setEnabled(false);
           IDSecretario.setEnabled(false);
           
               
           EnviarSecretario.setEnabled(false);
           NovoSecretario.setEnabled(true);
           EditarSecretario.setEnabled(false);
           CancelarSecretario.setEnabled(false);
        
           }
        PreencherTabela("select * from secretario order by Nomesecre");//CHAMA A TABELA LOGO AO ABRIR
    }//GEN-LAST:event_EnviarSecretarioActionPerformed

