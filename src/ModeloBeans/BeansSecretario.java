package ModeloBeans;

/**
 *
 * @author Victor
 */
public class BeansSecretario {
    
    private int CodSecre;
    private String NomeSecre;
    private String CpfSecre;
    private String TelSecre;
    private String PesquisaSecre;

    /**
     * @return the CodSecre
     */
    public int getCodSecre() {
        return CodSecre;
    }

    /**
     * @param CodSecre the CodSecre to set
     */
    public void setCodSecre(int CodSecre) {
        this.CodSecre = CodSecre;
    }

    /**
     * @return the NomeSecre
     */
    public String getNomeSecre() {
        return NomeSecre;
    }

    /**
     * @param NomeSecre the NomeSecre to set
     */
    public void setNomeSecre(String NomeSecre) {
        this.NomeSecre = NomeSecre;
    }

    /**
     * @return the CpfSecre
     */
    public String getCpfSecre() {
        return CpfSecre;
    }

    /**
     * @param CpfSecre the CpfSecre to set
     */
    public void setCpfSecre(String CpfSecre) {
        this.CpfSecre = CpfSecre;
    }

    /**
     * @return the TelSecre
     */
    public String getTelSecre() {
        return TelSecre;
    }

    /**
     * @param TelSecre the TelSecre to set
     */
    public void setTelSecre(String TelSecre) {
        this.TelSecre = TelSecre;
    }

    /**
     * @return the PesquisaSecre
     */
    public String getPesquisaSecre() {
        return PesquisaSecre;
    }

    /**
     * @param PesquisaSecre the PesquisaSecre to set
     */
    public void setPesquisaSecre(String PesquisaSecre) {
        this.PesquisaSecre = PesquisaSecre;
    }
    
}
