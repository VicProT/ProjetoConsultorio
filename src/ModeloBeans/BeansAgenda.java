public class BeansAgenda {
    
    private int CoddenAge;
    private int PacienteCodAge;
    private int DentistaCodAge;
    private String HorarioAge;
    private String ProcedimentoAge;
    private Date DataAge;
    private String StatusAge;
    private String NomeMedAge;
    private String NomePaciAge;

    public int getCoddenAge() {
        return CoddenAge;
    }

    public void setCoddenAge(int CoddenAge) {
        this.CoddenAge = CoddenAge;
    }

    public int getPacienteCodAge() {
        return PacienteCodAge;
    }

    public void setPacienteCodAge(int PacienteCodAge) {
        this.PacienteCodAge = PacienteCodAge;
    }

    public int getDentistaCodAge() {
        return DentistaCodAge;
    }

    public void setDentistaCodAge(int DentistaCodAge) {
        this.DentistaCodAge = DentistaCodAge;
    }

    public String getHorarioAge() {
        return HorarioAge;
    }

    public void setHorarioAge(String HorarioAge) {
        this.HorarioAge = HorarioAge;
    }

    public String getProcedimentoAge() {
        return ProcedimentoAge;
    }

    public void setProcedimentoAge(String ProcedimentoAge) {
        this.ProcedimentoAge = ProcedimentoAge;
    }

    public Date getDataAge() {
        return DataAge;
    }

    public void setDataAge(Date DataAge) {
        this.DataAge = DataAge;
    }

    public String getStatusAge() {
        return StatusAge;
    }

    public void setStatusAge(String StatusAge) {
        this.StatusAge = StatusAge;
    }

    public String getNomeMedAge() {
        return NomeMedAge;
    }

    public void setNomeMedAge(String NomeMedAge) {
        this.NomeMedAge = NomeMedAge;
    }

    public String getNomePaciAge() {
        return NomePaciAge;
    }

    public void setNomePaciAge(String NomePaciAge) {
        this.NomePaciAge = NomePaciAge;
    }
    
    
    
    
}
