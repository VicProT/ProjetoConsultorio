public class BeansPaciente {
    
    private Integer CoddenPaci;
    private String NomePaci;
    private String CPFPaci;
    private String TelPaci;
    private String RGPaci;
    private String ProcePaci;
    private String NasciPaci; 
    private String PesquisarPaciente;

    /**
     * @return the CoddenPaci
     */
    public Integer getCoddenPaci() {
        return CoddenPaci;
    }

    /**
     * @param CoddenPaci the CoddenPaci to set
     */
    public void setCoddenPaci(Integer CoddenPaci) {
        this.CoddenPaci = CoddenPaci;
    }

    /**
     * @return the NomePaci
     */
    public String getNomePaci() {
        return NomePaci;
    }

    /**
     * @param NomePaci the NomePaci to set
     */
    public void setNomePaci(String NomePaci) {
        this.NomePaci = NomePaci;
    }

    /**
     * @return the CPFPaci
     */
    public String getCPFPaci() {
        return CPFPaci;
    }

    /**
     * @param CPFPaci the CPFPaci to set
     */
    public void setCPFPaci(String CPFPaci) {
        this.CPFPaci = CPFPaci;
    }

    /**
     * @return the TelPaci
     */
    public String getTelPaci() {
        return TelPaci;
    }

    /**
     * @param TelPaci the TelPaci to set
     */
    public void setTelPaci(String TelPaci) {
        this.TelPaci = TelPaci;
    }

    /**
     * @return the RGPaci
     */
    public String getRGPaci() {
        return RGPaci;
    }

    /**
     * @param RGPaci the RGPaci to set
     */
    public void setRGPaci(String RGPaci) {
        this.RGPaci = RGPaci;
    }

    /**
     * @return the ProcePaci
     */
    public String getProcePaci() {
        return ProcePaci;
    }

    /**
     * @param ProcePaci the ProcePaci to set
     */
    public void setProcePaci(String ProcePaci) {
        this.ProcePaci = ProcePaci;
    }

    /**
     * @return the NasciPaci
     */
    public String getNasciPaci() {
        return NasciPaci;
    }

    /**
     * @param NasciPaci the NasciPaci to set
     */
    public void setNasciPaci(String NasciPaci) {
        this.NasciPaci = NasciPaci;
    }

    /**
     * @return the PesquisarPaciente
     */
    public String getPesquisarPaciente() {
        return PesquisarPaciente;
    }

    /**
     * @param PesquisarPaciente the PesquisarPaciente to set
     */
    public void setPesquisarPaciente(String PesquisarPaciente) {
        this.PesquisarPaciente = PesquisarPaciente;
    }

}
